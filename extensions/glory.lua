--[[
	效果：进入濒死时自动脱离并补满体力
]]--
gloryItemF = sgs.CreateTriggerSkill{
	name = "#gloryItemF",
	global = true,
	frequency = sgs.Skill_Compulsory,
	events = {sgs.EnterDying},
	on_trigger = function(self, event, player, data)
		local dying = data:toDying()
		local victim = dying.who
		if victim and victim:objectName() == player:objectName() then
			local room = player:getRoom()
			room:removePlayerMark(player, "glory_itemF", 1)
			local msg = sgs.LogMessage()
			msg.type = "#gloryItemF_Effect"
			msg.from = player
			room:sendLog(msg) --发送提示信息
			local recover = sgs.RecoverStruct()
			recover.who = player
			recover.recover = victim:getMaxHp() - victim:getHp()
			room:recover(victim, recover)
			return true
		end
		return false
	end,
	can_trigger = function(self, target)
		return target and target:isAlive() and target:getMark("glory_itemF") > 0 
	end,
	priority = 9,
}
AnJiang:addSkill(gloryItemF)