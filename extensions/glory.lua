--[[
	时机：确定伤害点数时
]]--
gloryConfirmDamage = sgs.CreateTriggerSkill{
	name = "#gloryConfirmDamage",
	global = true,
	frequency = sgs.Skill_Compulsory,
	events = {sgs.ConfirmDamage},
	on_trigger = function(self, event, player, data)
		local damage = data:toDamage()
		local source = damage.from
		if source and source:objectName() == player:objectName() then
			local room = player:getRoom()
			if room:getTag("GlorySystemClosed"):toBool() then
				return false
			end
			local thread = room:getThread()
			room:setTag("gloryData", data)
			thread:trigger(sgs.NonTrigger, room, source, sgs.QVariant("gloryConfirmDamage"))
			room:removeTag("gloryData")
		end
		return false
	end,
	can_trigger = function(self, target)
		return target ~= nil
	end,
	priority = 9,
}
--添加时机
AnJiang:addSkill(gloryConfirmDamage)
